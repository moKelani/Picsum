import UIKit

extension UIColor {

    class var basicRed: UIColor {
        return UIColor(red: 255/255, green: 0/255, blue: 0/255, alpha: 1.0)
    }
    
    class var basicYellow: UIColor {
        return UIColor(red: 252/255, green: 163/255, blue: 17/255, alpha: 1.0)
    }
    class var babyBlue: UIColor {
        return UIColor(red: 137/255, green: 207/255, blue: 240/255, alpha: 1.0)
    }
    class var navyBlue: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 128/255, alpha: 1.0)
    }
    class var cornflower: UIColor {
        return UIColor(red: 154/255, green: 206/255, blue: 235/255, alpha: 1.0)
    }
   
    
    
    

}
